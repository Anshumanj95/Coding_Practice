package Nestedforloop;

public class Table {
    public static void main(String[] args) {
        System.out.println("Tables 1 to 20");
        for (int i = 1; i <= 20; i++) {
            for (int j = 1; j <= 10; j++) {
                int k = i * j;
                System.out.print(k + " ");
            }
            System.out.println();

        }
    }
}






































































